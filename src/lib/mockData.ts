const generateSparklineData = (base: number, points: number, trend: number): { date: string; value: number }[] => {
  return Array.from({ length: points }, (_, i) => ({
    date: `T-${points - i}`,
    value: parseFloat((base + (Math.random() - 0.5) * (base / 10) + i * trend).toFixed(2)),
  }));
};
